#include <iostream>

using namespace std;

int main(){

    int i = 1;
    i = i + 1;
    cout << i;
    cout << endl;

    int j = 1;
    j++;
    cout << j;
    cout << endl;
    
    int k = 1;
    ++k;
    cout << k;
    cout << endl;

    int l = 1;
    l += 1;
    cout << l;
    cout << endl;

    return 0;
}